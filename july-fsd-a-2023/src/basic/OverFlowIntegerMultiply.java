package basic;

public class OverFlowIntegerMultiply {
    public static void main(String[] args) {
        int a = 100000; // 10^5
        int b = 100000; // 10^5
        System.out.println(a * b);

        long num1 = 100000;
        long num2 = 100000;
        System.out.println(num1 * num2);
    }
}
